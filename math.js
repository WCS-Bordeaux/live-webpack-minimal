function addition(a, b) {
    return a + b
}

function division(a, b) {
    return a / b
}

function multiplication(a, b) {
    return a * b
}

function modulo(a, b) {
    return a % b
}

function soustraction(a, b) {
    return a - b
}

function pow() {
    return Math.pow
}

export default {
    addition,
    division,
    multiplication,
    modulo,
    soustraction,
    pow
}
